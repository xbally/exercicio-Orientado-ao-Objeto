package sistemarh2;

/**
 *
 * @author gabri
 */
public abstract class pessoa {

    private String nome;
    private String rg;

    public pessoa(String nome, String rg) {
        this.nome = nome;
        this.rg = rg;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getRg() {
        return rg;
    }

    public void setRg(String rg) {
        this.rg = rg;
    }
    

}
